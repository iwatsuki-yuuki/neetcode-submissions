class Solution:
    def topKFrequent(self, nums: List[int], k: int) -> List[int]:
        # ① 頻度カウント用のハッシュマップを初期化
        count = {}
        # ② バケット配列を初期化
        #    「ちょうど i 回出現した数字たち」をリストで格納する
        #    サイズが len(nums) + 1 なのは、頻度の最大値が len(nums) のため
        #    (全要素が同じ数字の場合 = 頻度 n)。インデックス0〜n を使えるようにする
        #    [[] for i in range(...)] とリスト内包表記にしている理由:
        #    [[]] * n だと「同じリストの参照」が n 個並んでしまい、
        #    どこか1つに append すると全要素に反映されるバグが起こるため、
        #    内包表記で「独立した空リスト」を毎回生成している
        #    例 (len(nums) = 6 の場合):
        #      freq = [[], [], [], [], [], [], []]   ← 長さ7
        freq = [[] for i in range(len(nums) + 1)]
        # ③ nums をなめて、各数字の出現回数を数える
        for num in nums:
            count[num] = 1 + count.get(num, 0)
        # ④ count の中身をバケット配列 freq に振り分ける
        #    「数字 num の頻度が cnt なので、freq[cnt] に num を入れる」
        # =====================================================================
        for num, cnt in count.items():
            freq[cnt].append(num)

        # =====================================================================
        # ⑤ 結果格納用リスト
        # =====================================================================
        res = []

        # =====================================================================
        # ⑥ freq を「末尾(=高頻度)から先頭(=低頻度)」へ走査
        #    range(start, stop, step) の3引数版:
        #      start = len(freq) - 1   ← 最大インデックス(最高頻度)
        #      stop  = 0               ← 0 は含まない(つまり 1 まで走査)
        #      step  = -1              ← 1ずつ減らして逆順
        #
        #    なぜ逆順か:
        #    インデックス自体が頻度なので、後ろから見れば自動的に
        #    「最頻出から順」に取り出せる(ソート不要)
        #
        #    なぜ stop が 0 か:
        #    freq[0] は「0回出現した数字」を意味するが、
        #    そもそも nums に存在しないので走査する意味がない
        # =====================================================================
        for i in range(len(freq) - 1, 0, -1):
            # -----------------------------------------------------------------
            # ⑦ freq[i] には同じ頻度の数字が複数入っている可能性があるので
            #    その全部を res に追加していく
            # -----------------------------------------------------------------
            for num in freq[i]:
                res.append(num)

                # -------------------------------------------------------------
                # ⑧ k 個集まった瞬間に即リターン(早期終了)
                #    これにより、必要以上に freq を走査せずに済む
                #
                #    例 (k=2) のトレース:
                #      i=6,5,4 → freq[i] は空でスキップ
                #      i=3, num=1 → res = [1]      (len=1, 続行)
                #      i=2, num=2 → res = [1, 2]   (len=2, return!)
                #
                #    最終出力: [1, 2]
                # -------------------------------------------------------------
                if len(res) == k:
                    return res

        # =====================================================================
        # 補足: 関数末尾に return がないが、問題の制約により
        #       k <= distinct要素数 が保証されているため、
        #       上記ループ内で必ず return される。
        #       実務的には防御的に `return res` を書いておくとより安全。
        # =====================================================================